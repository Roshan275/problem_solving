#include <iostream>
using namespace std;

int main() {
    int i=20;
    cout<<"Initial value: "<<20<<endl;
    cout<<"During execution of post increment: "<<i++<<endl;
    cout<<"After post increment: "<<i<<endl<<endl;
    
    int j=20;
    cout<<"Initial value: "<<20<<endl;
    cout<<"During Execution of pre increment: "<<++j<<endl;
    cout<<"After pre increment: "<<j<<endl;
}
